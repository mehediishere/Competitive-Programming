#include<iostream>
using namespace std;

int main()
{

    int t, n, a[1005];

    cin>>t;

    while(t--)
    {
        cin>>n;
        a[n];
        for(int i=0; i<n; i++)
        {
            a[i] = 1;
        }
        for(int i=0; i<n; i++)
        {
            cout<<a[i]<<" ";
        }
        cout<<endl;

    }

    return 0;
}
